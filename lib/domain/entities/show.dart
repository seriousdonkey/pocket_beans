class Show {
  final int id;
  final String title;
  final String? description;
  final String? genre;
  final String thumbnailUrl;
  final String slideshowImageUrl;

  Show({
    required this.id,
    required this.title,
    required this.description,
    required this.genre,
    required this.thumbnailUrl,
    required this.slideshowImageUrl,
  });
}
